var x = 10;
var z = 11;

console.log(x > z); //lebih besar dari
console.log(x < z); //kurang dari
console.log(x >= z); //lebih besar dari sama dengan
console.log(x <= z); //kurang besar dari sama dengan
console.log(x == z); // sama dengan
console.log(x === z); // sama dengan dan tipe data harus sama
console.log(x != z); // tidak sama dengan
console.log(x !== z); // tidak sama dengan dan tipe data harus sama
